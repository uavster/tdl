; Returns the time constant for a given sample rate
;
; INPUT  : EAX = Sample rate
; OUTPUT : AL = Time constant
;컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴컴
GetTimeCt       proc
        mov     ebx,eax
        mov     eax,1000000
        xor     edx,edx
        div     ebx
        sub     eax,256
        neg     eax
        ret
GetTimeCt       endp

